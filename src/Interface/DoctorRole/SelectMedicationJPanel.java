    private void populateInventoryTable(){
        HospitalEnterprise he = (HospitalEnterprise) this.enterprise;
        
        DefaultTableModel dtm = (DefaultTableModel)drugInventoryTable.getModel();
        int rowCount = drugInventoryTable.getRowCount();
        for(int i = rowCount - 1; i >= 0; i--){
            dtm.removeRow(i);
        }
        
        for(DrugReceived drug: he.getDruginventory().getReceviedDrugList()){
            Object row[] = new Object[4];
            row[0] = drug;
            row[1] = drug.getDrug().getName();
            row[2] = drug.getQuantity();
            row[3] = drug.getDrug().getPrice() + "$";
            dtm.addRow(row);
        }
    }
    
    private void populateDrugSelectTable(){       
        this.drugHashtable = this.doctoruseraccount.getDoctoraccount().getDrugHashtable();
        ArrayList<DrugReceived> druglist = this.drugHashtable.get(this.patientuseraccount);
        if(this.drugHashtable.get(this.patientuseraccount) == null){
            this.drugHashtable.put(this.patientuseraccount, new ArrayList<DrugReceived>());
            druglist = this.drugHashtable.get(this.patientuseraccount);
        }
        
        DefaultTableModel dtm = (DefaultTableModel)this.drugSelectTable.getModel();
        int rowCount = this.drugSelectTable.getRowCount();
        for(int i = rowCount - 1; i >= 0; i--){
            dtm.removeRow(i);
        }
        
        int totalamount = 0;
        for(DrugReceived drug: druglist){
            Object row[] = new Object[4];
            row[0] = drug;
            row[1] = drug.getDrug().getName();
            row[2] = drug.getQuantity();
            row[3] = drug.getDrug().getPrice() + "$";
            totalamount = totalamount + drug.getQuantity() * drug.getDrug().getPrice();
            dtm.addRow(row);
        }       
        this.totalTxt.setText(String.valueOf(totalamount) + "$");
    }

