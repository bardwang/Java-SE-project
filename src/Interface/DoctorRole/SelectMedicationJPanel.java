    private void addDrugBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDrugBtnActionPerformed
        // TODO add your handling code here:
        
        if(!this.quantityTxt.getText().matches("^\\d+$")){
        JOptionPane.showMessageDialog(null, "Please type a integer for priceTextField!");
        return;
        }        
        
        int selectedRow = drugInventoryTable.getSelectedRow();
        if(selectedRow >= 0){
            DrugReceived drug = (DrugReceived)drugInventoryTable.getValueAt(selectedRow, 0);
            int quantity = Integer.parseInt(this.quantityTxt.getText());
            if(quantity <= 0){
                JOptionPane.showMessageDialog(null, "Input wrong number!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }else if(drug.getQuantity() < quantity){
                JOptionPane.showMessageDialog(null, "Not enough drug!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }else{
            drug.setQuantity(drug.getQuantity() - quantity);
            ArrayList<DrugReceived> drugs = this.drugHashtable.get(this.patientuseraccount);          
            DrugReceived drugSelected = new DrugReceived();
            drugSelected.setDrug(drug.getDrug());
            drugSelected.setSerialNumber(drug.getSerialNumber());
            drugSelected.setQuantity(quantity);
            drugSelected.setExpirationDate(drug.getExpirationDate());        
            drugs.add(drugSelected);
            this.drugHashtable.put(this.patientuseraccount, drugs);
            
            this.populateInventoryTable();
            this.populateDrugSelectTable();
            this.quantityTxt.setText("");
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_addDrugBtnActionPerformed

    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = drugSelectTable.getSelectedRow();
        if(selectedRow >= 0){
            DrugReceived drugSelected = (DrugReceived)drugSelectTable.getValueAt(selectedRow, 0);
            ArrayList<DrugReceived> drugsSelected = this.drugHashtable.get(this.patientuseraccount);
            drugsSelected.remove(drugSelected);
            
            HospitalEnterprise he = (HospitalEnterprise) this.enterprise;
            for(DrugReceived drug: he.getDruginventory().getReceviedDrugList()){
            int quantity = drugSelected.getQuantity();
            if(drug.getSerialNumber().equals(drugSelected.getSerialNumber())){
                drug.setQuantity(quantity + drug.getQuantity());
                break;
                }
            }
            this.populateInventoryTable();
            this.populateDrugSelectTable();
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_removeBtnActionPerformed

