    private void populateNetworkComboBox(){
        networkComboBox.removeAllItems();
        
        for (Network network : system.getNetworkList()){
            networkComboBox.addItem(network);
        }
    }
    
    private void populateEnterpriseComboBox(){
        enterpriseComboBox.removeAllItems();
        
        if(network != null){
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            enterpriseComboBox.addItem(enterprise);
            }
        }
    }
    
    private void populateOrganizationComboBox(){
        organizationComboBox.removeAllItems();
        
        if(enterprise != null){
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(role.equals("Manager")){
                if(organization instanceof ManagerOrganization){
                    organizationComboBox.addItem(organization);
                }
            }else if(role.equals("HR Manager")){
                if(organization instanceof HROrganization){
                    organizationComboBox.addItem(organization);
                }
            }else if(role.equals("Admin")){
                if(organization instanceof AdminOrganization){
                    organizationComboBox.addItem(organization);
                }
            }else if(role.equals("Drug Admin")){
                if(organization instanceof DrugAdminOrganization){
                    organizationComboBox.addItem(organization);
                }
            }else if(role.equals("Doctor")){
                if(organization instanceof DoctorOrganization){
                    organizationComboBox.addItem(organization);
                }
            }
            }
        }
    }
    
