    private void continueBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continueBtnActionPerformed
        // TODO add your handling code here:
        if(this.network == null){
            JOptionPane.showMessageDialog(null, "There is no network!");
            return;
        }
        
        if(this.enterprise == null){
            JOptionPane.showMessageDialog(null, "There is no enterprise!");
            return;
        }
        
        if(this.organization == null){
            JOptionPane.showMessageDialog(null, "There is no organization!");
            return;
        }
  
        UserAccountListJPanel userAccountListJPanel = new UserAccountListJPanel(userProcessContainer, system, organization, role);      
        userProcessContainer.add("UserAccountListJPanel", userAccountListJPanel);      
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);
    }//GEN-LAST:event_continueBtnActionPerformed

