     public void populateTable(EcoSystem system){
        DefaultTableModel model = (DefaultTableModel) JTable.getModel();
        
        model.setRowCount(0);
        
        for (Network network : system.getNetworkList()){
            for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){

                    Object[] row = new Object[4];
                    row[0] = organization;
                    row[1] = OrganizationType(organization);
                    row[2] = enterprise;
                    row[3] = network.getName();
                    model.addRow(row);
                }
            }
        }
    }
     
    private Type OrganizationType(Organization organization){
        if(organization instanceof AdminOrganization){
            return Type.ADMIN;
        }else if(organization instanceof DoctorOrganization){
            return Type.DOCTOR;
        }else if(organization instanceof HROrganization){
            return Type.HR;
        }else{
            return Type.DRUGADMIN;
        }
    } 

