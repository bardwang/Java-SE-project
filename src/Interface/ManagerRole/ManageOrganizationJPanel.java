    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        // TODO add your handling code here:
        int selectRow = this.JTable.getSelectedRow();
        if(selectRow >= 0){
            int dialogButton = JOptionPane.YES_NO_CANCEL_OPTION;     
            
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the organization", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Organization o = (Organization)JTable.getValueAt(selectRow, 0);
                Enterprise ep = (Enterprise)JTable.getValueAt(selectRow, 2);
                ep.getOrganizationDirectory().getOrganizationList().remove(o);
                populateTable(system);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_removeBtnActionPerformed

