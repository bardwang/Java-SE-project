    private void viewDetailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailBtnActionPerformed
        // TODO add your handling code here:
        int selectRow = this.JTable.getSelectedRow();
        if(selectRow >= 0){
            UserAccount useraccount = (UserAccount) this.JTable.getValueAt(selectRow, 0);
            ViewDoctorDetailJPanel viewDoctorDetailJPanel = new ViewDoctorDetailJPanel(userProcessContainer, system, useraccount);      
            userProcessContainer.add("ViewDoctorDetailJPanel", viewDoctorDetailJPanel);      
            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
            cardLayout.next(userProcessContainer);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_viewDetailBtnActionPerformed

    private void hireBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hireBtnActionPerformed
        // TODO add your handling code here:
        int selectRow = this.JTable.getSelectedRow();
        if(selectRow >= 0){
            int dialogButton = JOptionPane.YES_NO_CANCEL_OPTION;     
            
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to hire this doctor?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                UserAccount useraccount = (UserAccount) this.JTable.getValueAt(selectRow, 0);
                useraccount.getDoctoraccount().setHired(Boolean.TRUE);
                useraccount.setRole(new HiredDoctorRole());
                // remove from the system
                this.system.getUserAccountDirectory().getUserAccountList().remove(useraccount);
                // add to the enterprise
                Enterprise enterprise = useraccount.getEnterprise();
                HireStepJPanel hireStepJPanel = new HireStepJPanel(userProcessContainer, system, enterprise, useraccount);      
                userProcessContainer.add("HireStepJPanel", hireStepJPanel);      
                CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                cardLayout.next(userProcessContainer);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_hireBtnActionPerformed

    private void rejectBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectBtnActionPerformed
        // TODO add your handling code here:
        int selectRow = this.JTable.getSelectedRow();
        if(selectRow >= 0){
            int dialogButton = JOptionPane.YES_NO_CANCEL_OPTION;     
            
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to reject this doctor", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                UserAccount useraccount = (UserAccount) this.JTable.getValueAt(selectRow, 0);
                useraccount.getDoctoraccount().setRejected(Boolean.TRUE);
                useraccount.setRole(new RejectedDoctorRole());
                JOptionPane.showMessageDialog(null, "This doctor has been rejected.");
                this.populateTable(system);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_rejectBtnActionPerformed


