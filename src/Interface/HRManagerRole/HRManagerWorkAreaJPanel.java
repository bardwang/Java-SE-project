    public void populateTable(EcoSystem system){
        
        DefaultTableModel model = (DefaultTableModel) JTable.getModel();
        
        model.setRowCount(0);
        
        for (UserAccount useraccount : system.getUserAccountDirectory().getUserAccountList()){
            if(useraccount.getDoctoraccount() != null && useraccount.getDoctoraccount().getHired() == false && useraccount.getDoctoraccount().getRejected() == false){
            Object[] row = new Object[6];
            row[0] = useraccount;
            row[1] = useraccount.getAge();
            row[2] = useraccount.getDoctoraccount().getDegree();
            row[3] = useraccount.getDoctoraccount().getYearsofexperience();
            row[4] = useraccount.getEnterprise();
            row[5] = useraccount.getNetwork().getName();
            model.addRow(row);
            }
        }
    }

